public class triangle {
    public static void main(String[] args) {
        int j, n = 1;
        for (j = 0; j < 5; j++) {
            int i = 0;
            while (i < n) {
                System.out.print("*");
                i++;
            }
            System.out.print("\n");
            n++;
        }
    }
}
